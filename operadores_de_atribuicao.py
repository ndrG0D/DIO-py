# Atribuição simples
x = 10
y = 5

# Atribuição com operadores aritméticos
x += 3  # x = x + 3
y -= 2  # y = y - 2
x *= 2  # x = x * 2
y /= 3  # y = y / 3

# Atribuição com operadores bit a bit
a = 5
a <<= 1  # a = a << 1
a >>= 2  # a = a >> 2
a &= 3   # a = a & 3
a |= 2   # a = a | 2
a ^= 1   # a = a ^ 1

# Atribuição com operadores de módulo e exponenciação
b = 10
b %= 3  # b = b % 3
b **= 2  # b = b ** 2

# Atribuição com operadores de divisão inteira
c = 15
c //= 4  # c = c // 4

# Exibindo os resultados
print("x:", x)
print("y:", y)
print("a:", a)
print("b:", b)
print("c:", c)
