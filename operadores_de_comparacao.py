# Tabela de Operadores de Comparação em Python

# Operador    Descrição                          Exemplo
# -----------------------------------------------------------------
# ==          Igual a                            5 == 5   -> True
# !=          Diferente de                       5 != 3   -> True
# >           Maior que                          5 > 3    -> True
# <           Menor que                          3 < 5    -> True
# >=          Maior ou igual a                   5 >= 5   -> True
# <=          Menor ou igual a                   3 <= 5   -> True

# Exemplos de uso:
a = 5
b = 3

print(a == b)  # False
print(a != b)  # True
print(a > b)   # True
print(a < b)   # False
print(a >= b)  # True
print(a <= b)  # False
# -----------------------------------------------------------------