# Exemplo 1: Verificando se um elemento está presente em uma lista
frutas = ['melao', 'banana', 'laranja', 'mexirica']
resultado = 'mexirica' in frutas
print(f"'mexirica' esta na lista de frutas? {resultado}")

# Exemplo 2: Verificando se uma substring está presente em uma string
texto = "Eu odeio escrever exemplos, mas tem que ser feito."
resultado = 'odeio' in texto
print(f"'odeio' esta na string? {resultado}")