# Tabela de Operadores Aritméticos em Python

# Adição
a = 10
b = 5
resultado_adicao = a + b  # Soma de a e b
print(f'Adição: {a} + {b} = {resultado_adicao}')

# Subtração
resultado_subtracao = a - b  # Subtração de b de a
print(f'Subtração: {a} - {b} = {resultado_subtracao}')

# Multiplicação
resultado_multiplicacao = a * b  # Multiplicação de a e b
print(f'Multiplicação: {a} * {b} = {resultado_multiplicacao}')

# Divisão
resultado_divisao = a / b  # Divisão de a por b
print(f'Divisão: {a} / {b} = {resultado_divisao}')

# Divisão Inteira
resultado_divisao_inteira = a // b  # Divisão inteira de a por b
print(f'Divisão Inteira: {a} // {b} = {resultado_divisao_inteira}')

# Módulo
resultado_modulo = a % b  # Resto da divisão de a por b
print(f'Módulo: {a} % {b} = {resultado_modulo}')

# Exponenciação
resultado_exponenciacao = a ** b  # a elevado à potência de b
print(f'Exponenciação: {a} ** {b} = {resultado_exponenciacao}')